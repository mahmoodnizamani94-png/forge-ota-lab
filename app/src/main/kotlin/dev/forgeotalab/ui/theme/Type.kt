/**
 * Forge typography system — "dense, technical, warm" per the PRD design identity.
 *
 * Uses Inter via Google Fonts downloadable provider with system sans-serif fallback.
 * WHY Inter: geometric sans-serif that reads well at small sizes in data-dense
 * extraction UIs. Tabular figures by default. Excellent x-height for on-screen text.
 *
 * WHY Google Fonts provider (not bundled): Keeps APK lean while caching the font
 * after first download. Fallback to platform sans-serif ensures offline-first UX.
 *
 * Scale design choices:
 *   - Display: generous for status headings, tight letter-spacing for impact
 *   - Body: slightly compressed line-height (1.375×) for data-dense layouts
 *   - Labels: medium weight for toolbar and metadata, generous letter-spacing
 */

private val googleFontProvider = GoogleFont.Provider(
    providerAuthority = "com.google.android.gms.fonts",
    providerPackage = "com.google.android.gms",
    certificates = R.array.com_google_android_gms_fonts_certs,
)

private val interGoogleFont = GoogleFont("Inter")

/**
 * Inter font family with weight variants.
 * Falls back to platform default sans-serif if Google Fonts unavailable.
 */
val InterFontFamily = FontFamily(
    Font(
        googleFont = interGoogleFont,
        fontProvider = googleFontProvider,
        weight = FontWeight.Normal,
    ),
    Font(
        googleFont = interGoogleFont,
        fontProvider = googleFontProvider,
        weight = FontWeight.Medium,
    ),
    Font(
        googleFont = interGoogleFont,
        fontProvider = googleFontProvider,
        weight = FontWeight.SemiBold,
    ),
    Font(
        googleFont = interGoogleFont,
        fontProvider = googleFontProvider,
        weight = FontWeight.Bold,
    ),
)

